package com.jetbrains.editing;

// four of the most useful live templates
// `main` to create the Java main method
// `iter` to create a iterable Iterate of an array
// `for` or `fori` to create a for loop
// `sout` to create a System.out.println statement
public class LiveTemplates {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("i = " + i);
        }

    }
}
